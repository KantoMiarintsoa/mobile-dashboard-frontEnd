export function t(key: string, locale: Locale, params?: Record<string, string>): string {
  let text = translations[locale]?.[key] || translations.fr[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, v);
    }
  }
  return text;
}
